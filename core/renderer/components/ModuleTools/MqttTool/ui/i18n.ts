const LOCALE_KEY = 'devtoolbox_locale';

function getLang(): 'en' | 'zh' {
  try {
    const stored = String(localStorage.getItem(LOCALE_KEY) ?? '').trim().toLowerCase();
    if (stored === 'zh' || stored.startsWith('zh-')) return 'zh';
    if (stored) return 'en';
  } catch {
    // ignore
  }
  const nav = typeof navigator !== 'undefined' ? String(navigator.language ?? '') : '';
  return nav.toLowerCase().includes('zh') ? 'zh' : 'en';
}

export function t(key: keyof typeof en, vars?: Record<string, string>): string {
  const dict = getLang() === 'zh' ? zh : en;
  const raw = dict[key] ?? en[key] ?? String(key);
  if (!vars) return raw;
  return Object.keys(vars).reduce((acc, k) => acc.split(`{${k}}`).join(vars[k]), raw);
}
